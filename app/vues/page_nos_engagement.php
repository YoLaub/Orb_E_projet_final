        <h1>Nos Engagements</h1>

        <h2>Éco-responsabilité</h2>
        <p>AY-Lab s'engage à adopter une démarche éco-responsable en utilisant les ressources de manière optimale et en minimisant son impact environnemental. Nos stratégies incluent :</p>
        <ul>
            <li>Réduction du poids des images et utilisation de formats modernes.</li>
            <li>Limitation de l'utilisation de vidéos et d'animations lourdes.</li>
            <li>Minification et compression des fichiers CSS et JavaScript.</li>
            <li>Choix d'un hébergeur éco-responsable utilisant des énergies renouvelables.</li>
        </ul>

        <h2>Sécurité</h2>
        <p>La sécurité est une priorité pour AY-Lab. Nous mettons en œuvre les mesures suivantes :</p>
        <ul>
            <li>Système d'identification et gestion des rôles pour restreindre les accès.</li>
            <li>Filtrage et validation des données soumises par les utilisateurs.</li>
            <li>Utilisation de requêtes préparées pour éviter les injections SQL.</li>
            <li>Sécurisation des mots de passe avec des fonctions de hachage.</li>
        </ul>


