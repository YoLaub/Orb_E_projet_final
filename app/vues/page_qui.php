        <h1>Qui sommes nous ?</h1>

        <h2>AY-Lab : L'innovation au service de l'humain</h2>
        <p>AY-Lab est née de la rencontre entre des experts issus de domaines complémentaires, réunis par une passion commune pour l’innovation technologique. Cette start-up a pour mission de repousser les limites de l’intelligence artificielle en développant des solutions d’assistance intelligentes et accessibles, conçues pour améliorer le quotidien de chacun. Guidée par une vision humaniste, AY-Lab place l’utilisateur au cœur de ses innovations, avec une ambition claire : rendre la technologie plus intuitive et bénéfique pour tous.</p>

        <img src="./publique/images/mini_jeu.webp" alt="Bague qui represente notre engagement">

        <h2>Orb’E : L’Assistant ultime</h2>
        <p>Parmi nos réalisations phares, Orb’E incarne l’avenir de l’assistance personnelle. Cette sphère élégante et lisse, dotée de mini LEDs formant un visage expressif, offre une interaction naturelle et engageante. Grâce à une caméra révolutionnaire, Orb’E analyse son environnement en temps réel pour proposer conseils, divertissement et accompagnement personnalisé, s’adaptant continuellement aux besoins de son utilisateur.</p>

        <h2>Une technologie de pointe</h2>
        <ul>
            <li><strong>IA de nouvelle génération</strong> : Propulsé par Lya, une intelligence artificielle avancée, Orb’E apprend et évolue selon les habitudes de son utilisateur, offrant une expérience unique et sur-mesure.</li>
            <li><strong>Antigravitation</strong> : Grâce à une technologie révolutionnaire, Orb’E flotte librement en annulant les effets de la gravité terrestre, lui permettant de se déplacer en toute fluidité.</li>
            <li><strong>Énergie lumineuse</strong> : Orb’E fonctionne principalement grâce à la lumière, garantissant ainsi une autonomie optimale et un impact écologique réduit. Son système de refroidissement intégré assure des performances maximales en toute circonstance.</li>
        </ul>

        <h2>Sécurité et confidentialité avant tout</h2>
        <p>Chez AY-Lab, nous accordons une importance capitale à la protection des données personnelles. Orb’E fonctionne sans connexion Internet pour ses usages quotidiens, garantissant ainsi la confidentialité des informations. Un protocole de connexion unique est activé uniquement pour les mises à jour. Lorsque des interactions réseau sont nécessaires, Orb’E crée un environnement virtuel sécurisé et utilise le réseau de l’objet connecté le plus proche.</p>

