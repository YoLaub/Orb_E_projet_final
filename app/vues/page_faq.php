        <h3>Qu'est-ce qu'Orb'E ?</h3>
        <p>Orb'E est un assistant personnel intelligent conçu par AY-Lab. Il est équipé de capacités d'expression grâce à des mini LEDs et utilise une technologie d'antigravitation pour se déplacer librement.</p>

        <h3>Comment fonctionne la technologie d'antigravitation d'Orb'E ?</h3>
        <p>Orb'E utilise une technologie révolutionnaire pour annuler les effets de la gravité terrestre, lui permettant de flotter et de se déplacer silencieusement dans n'importe quel environnement.</p>

        <h3>Quelles sont les mesures de sécurité mises en place pour protéger mes données ?</h3>
        <p>AY-Lab accorde une importance primordiale à la sécurité des données. Orb'E fonctionne sans connexion Internet pour une utilisation générale, et toutes les données sensibles sont cryptées et traitées avec soin.</p>

        <h3>Comment puis-je commander un Orb'E ?</h3>
        <p>Vous pouvez commander Orb'E directement sur notre site internet. Sélectionnez le produit, ajoutez-le à votre panier, puis suivez les instructions pour finaliser votre commande.</p>


